     public void supprimerLotPartie1() {

        StockBrut[] lots = FichierStockBrut.lire(Atelier_Fabrication.dir);

        String supprime = jTextField1.getText();

        int tailleLots = lots.length;
        // on suppose que la gamme a supprimer n'existe pas
        boolean test = false;

        for (int k = 0; k < tailleLots; k++) {                                       // on parcours le tableau gammes
            if (supprime.equals(lots[k].getrefLot())) {                      // si la gamme à supprimer correspond à une référence dans le tableau
                test = true;                                      // on indique que la gamme à supprimer existe
            }
        }
        if (test) {
            this.afficherLot(supprime);
        } else {
            jTextArea1.append("Cet lot n'existe pas" + "\n");
            jTextArea1.append("Try again ;)" + "\n");
        }

    }

    public void supprimerLotPartie2() {

        StockBrut[] lots = FichierStockBrut.lire(Atelier_Fabrication.dir);
        String supprime = jTextField1.getText();
        int tailleLots = lots.length;
        StockBrut[] lotsConserver = new StockBrut[tailleLots - 1];                     // on crée un tableau de taille -1
        int j = 0;
        for (int i = 0; i < tailleLots; i++) {                                       // on parcours le tableau gammes
            if (!supprime.equals(lots[i].getrefLot())) {
                lotsConserver[j] = lots[i];                                    // on insère dans le nouveau tableau les gammes sauf celle à supprimer
                j++;
            }
        }

        lots = lotsConserver;                                                  // le nouveau tableau écrase l'ancien
        jTextArea1.append("Le lot " + supprime + " a ete suprimee"+"\n");

        FichierStockBrut.enregistrer(Atelier_Fabrication.dir, lots);

    }
    
    
        public void afficherLot(String modif) {


        StockBrut[] lots = FichierStockBrut.lire(Atelier_Fabrication.dir);
        int tailleLots = lots.length;
        for (int i = 0; i < tailleLots; i++) {                                       // on parcours tous le tableau
            if (modif.equals(lots[i].getrefLot())) {                        // mais la seule gamme qui sera affichée doit être la même que saisie par l'utilisateur
                StockBrut lot = lots[i];
                this.afficheL(lot);
            }
        }
    }

    

    public void afficheL(StockBrut lot) {
        jTextArea1.append("===== Lot =====" + "\n");
        jTextArea1.append("Référence du lot : " + lot.getrefLot() + "\n");
        jTextArea1.append("Type : " + lot.gettype() + "\n");
        jTextArea1.append("Materiaux : " + lot.getmateriaux() + "\n");
        jTextArea1.append("Quantité disponible : " + lot.getquantiteDispo() + "\n");
         jTextArea1.append("Prix Unitaire : " + lot.getprixUnitaire() + "\n");
        jTextArea1.append("===================" + "\n");
    }

    
    
    
    
